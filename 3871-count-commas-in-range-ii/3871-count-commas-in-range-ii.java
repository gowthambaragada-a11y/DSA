class Solution {
    public long countCommas(long n) {
        if(n<1000)return 0;
        if(n<1000000L)return n-999;
        if(n<1000000000L)return (n-999999L)*2+999999L-999;
        if(n<1000000000000L)return (n-999999999L)*3+(999999999L-999999)*2+999999L-999;
        if(n<1000000000000000L)return (n-999999999999L)*4+(999999999999L-999999999L)*3+(999999999L-999999)*2+999999L-999;
        if(n==1000000000000000L)return (n-999999999999L)*4+(999999999999L-999999999L)*3+(999999999L-999999)*2+999999L-999+1;
        return -1;
    }
}